package vigraha.admin.domain;

public class Company {
    private String companyName;
    private String regNo;
    private String teleOne;
    private String teleTwo;
    private String address;
    private String email;
    private String username;
    private String password;

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getRegNo() {
        return regNo;
    }

    public void setRegNo(String regNo) {
        this.regNo = regNo;
    }

    public String getTeleOne() {
        return teleOne;
    }

    public void setTeleOne(String teleOne) {
        this.teleOne = teleOne;
    }

    public String getTeleTwo() {
        return teleTwo;
    }

    public void setTeleTwo(String teleTwo) {
        this.teleTwo = teleTwo;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}

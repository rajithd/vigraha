package vigraha.rule.domain;


public class VoiceCall {

    private String numberMakingCall;
    private String callResult;

    public String getNumberMakingCall() {
        return numberMakingCall;
    }

    public void setNumberMakingCall(String numberMakingCall) {
        this.numberMakingCall = numberMakingCall;
    }

    public String getCallResult() {
        return callResult;
    }

    public void setCallResult(String callResult) {
        this.callResult = callResult;
    }
}
